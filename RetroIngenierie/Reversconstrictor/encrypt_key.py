def encode_password(password):
    a = b''
    x_list = [110, -34, -230]
    for i in range(len(password)):
        b = password[i]//11 + 11
        c = password[i] % 11
        d = b+c
        e = b*c
        r = []
        for i in range(3):
            x = x_list[i]
            y = x**2 - d*x + e
            assert y > 0 and y < 65536 and y not in r
            r.append(y)
        for i in range(3):
            a += bytes.fromhex(f"{r[i]//256:02x}")
            a += bytes.fromhex(f"{r[i]%256:02x}")
    return a
